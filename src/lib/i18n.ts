type StringKey = keyof typeof strings.en;

const categoryKeys: Record<string, StringKey> = {
  'Change':   'cat_change',
  'Design':   'cat_design',
  'Learn':    'cat_learn',
  'System':   'cat_system',
  'Culture':  'cat_culture',
  'Strategy': 'cat_strategy',
  'Impact':   'cat_impact',
  'Archive':  'cat_archive',
};

export function tCategory(lang: AppLanguage, englishLabel: string): string {
  const key = categoryKeys[englishLabel];
  return key ? t(lang, key) : englishLabel;
}

export function t(lang: AppLanguage, key: StringKey, vars?: Record<string, string | number>): string {
  let str: string = strings[lang]?.[key] ?? strings.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
